# schema在数据库领域，表示数据库对象的集合，包含各种对象如：表、视图、存储过程和索引等。

# SQL 标准
# 一个数据库（Database）可包含多个模式（Schema），模式是数据库对象的逻辑容器。

# Oracle
# Schema 与用户（User）强绑定。创建用户时就等于创建了一个同名 Schema。用户（user）就是 schema 的拥有者，用户名称与 schema 名通常一致。一般一个用户对应一个集合，为了区分，需给不同集合起名。其实一个用户/角色可拥有多个 schema，多个用户也可以访问同一个 schema（通过权限授权）。

# 用户的schema名相当于用户名，并作为该用户的缺省schema。所以schema集合看上去像用户名。如访问一个数据表时，若该表未指明属于哪个schema，系统会自动加上默认schema：

# 一、定义与作用
# ‌定义‌：schema是数据库中用于组织和管理数据库对象的逻辑结构，它包含了数据库中的表、视图、索引、关系以及存储过程等对象的集合。
#
# ‌作用‌：
#
# ‌组织和管理数据库对象‌：schema提供了一种将数据库对象（如表、视图等）进行分组和组织的方式，使得数据库的结构更加清晰和易于管理。
# ‌权限管理‌：每个schema都有其独立的命名空间，可以由不同的用户或角色拥有，从而方便对权限进行细粒度的控制。
# ‌数据隔离‌：通过不同的schema，可以将不同应用程序或用户的数据进行隔离，避免命名冲突和数据混淆。
# 二、schema的组成
# schema主要由以下几类对象组成：
#
# ‌表‌：数据库中最基本的对象，用于存储数据。表由行和列组成，每一行代表一条记录，每一列代表一个字段。
# ‌视图‌：对一个或多个表的虚拟展示，它是一个查询的结果集。视图不存储数据，只是基于表的一种逻辑表示。
# ‌索引‌：对表中一列或多列的排序列表，用于加速查询速度。索引可以大大提高数据库的查询效率。
# ‌关系‌：定义了表与表之间的联系，如外键约束等。这些关系有助于保持数据的一致性和完整性。
# ‌存储过程‌：预编译的SQL代码，用于执行特定的数据库操作。存储过程可以提高数据库操作的效率和安全性。
# 三、schema的使用
# 在数据库管理系统中，可以使用SQL语句来创建、使用、删除和修改schema。例如：
#
# ‌创建schema‌：使用CREATE SCHEMA语句来创建一个新的schema。
# ‌使用schema‌：在SQL语句中可以通过在表名前加上schema名称来引用该schema下的表。
# ‌设置默认schema‌：在某些数据库管理系统中，可以设置默认schema，这样在查询时不需要每次指定schema名称。
# ‌删除schema‌：使用DROP SCHEMA语句来删除一个schema及其包含的所有对象。
# 四、schema的重要性
# schema在数据库设计和管理中起着至关重要的作用。它不仅是数据库的结构定义，更是数据完整性、一致性和安全性的保障。通过合理的schema设计，可以大大提高数据库的性能、可维护性和可扩展性。
